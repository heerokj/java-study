package day01;

public class VarEx2 {
	public static void main(String[] args) {
		
		
		int i = 20; // int형 변수 i에 20값 대입 
		System.out.println("i : " + i); // 출력
		
		float h = 182.3f; // float형 변수 h에 182.3값 대입 
		System.out.println(h); // 출력
		
		//나이 : 20살
		//키 : 182.3
		System.out.println("나이:" +  i+"살");
		System.out.println("키: " + h);
		
		
		
	}

}
