package day01;

//4. 비교연산자
// >, <, >=, <=, ==, !=

public class OperEx4 {

	public static void main(String[] args) {
		
		int a = 100;
		int b = 20;
		
		System.out.println("a>b : " + (a > b));
		System.out.println("a<b : " + (a < b));
		System.out.println("a>=b : " + (a >= b));
		System.out.println("a<=b : " + (a <= b));
		System.out.println("a==b : " + (a == b));
		System.out.println("a!=b : " + (a != b));

		/* 순서대로 출력값 
		 * 
		 * a>b : true 
		 * a<b : false 
		 * a>=b : true 
		 * a<=b : false 
		 * a==b : false 
		 * a!=b : true
		 * 
		 */

	}

}
