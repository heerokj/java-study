package day02;

public class StatementTest03 {

	public static void main(String[] args) {

		for (int i = 0; i < 10; i++) {
			System.out.println(2 * i + 1);
		}

		for (int i = 1; i <= 20; i += 2) {
			System.out.println(i);
		}

		String str = "*";
		System.out.println(str);

		str = str + "*";
		System.out.println(str);

	}

}
